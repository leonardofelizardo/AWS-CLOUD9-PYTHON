"""
Uso de tipos de dados numéricos
"""
print("Python has three numeric types: int, float, and complex")
"""
Criação de uma variável
"""
myValue=1
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
"""
Apresentação do tipo de dado float
"""
myValue=3.14
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
"""
Apresentação do tipo de dado complex
"""
myValue=5j
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
"""
Apresentação do tipo de dado bool
"""
myValue=True
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
myValue=False
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))