"""
Uso de listas, tuplas e dicionários
"""
"""
Definição de uma lista
"""
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))
"""
Acesso a uma lista pela posição
"""
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])
"""
Alteração dos valores da lista
"""
myFruitList[2] = "orange"
print(myFruitList)
myFinalAnswerTuple = ("apple", "banana", "pineapple")
"""
Apresentação do tipo de dado tupla / Definição de uma tupla
"""
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))
"""
Acesso a uma tupla pela posição
"""
print(myFinalAnswerTuple[0])
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])
"""
Apresentação do tipo de dado dicionário / Definição de um dicionário
"""
myFavoriteFruitDictionary = {
    "leo" : "apple",
    "gab" : "pineapple",
    "lipe" : "banana"
    }
print(myFavoriteFruitDictionary)
print(type(myFavoriteFruitDictionary))
"""
Acesso a um dicionário pelo nome
"""
print(myFavoriteFruitDictionary["leo"])
print(myFavoriteFruitDictionary["gab"])
print(myFavoriteFruitDictionary["lipe"])